Ce plugin ATOS permet de gérer un nombre important de paiement sécurisé bancaire.


INSTALLATION
============

- copiez les binaires fournis par ATOS dans le répertoire bin/
  il s'agit de request et response

- renommez les fichier certif.fr.xxxxxxxxxxxxxxx et parcom.xxxxxxxxxxxxxxx
   remplacez tout simplement les xxxxxxxxxxxxxxx par votre n° de marchand

- editez le fichier config.php et inscrivez votre n° de marchand.
